# name.rb

# Ask the user for their name
print "What is your name? "

# Get the user's input and store it in a variable
name = gets.chomp

# Print out a greeting message with the user's name included
puts "Hello, #{name}! Nice to meet you."

# Print out the user's name 10 times
10.times do
    puts name
end

